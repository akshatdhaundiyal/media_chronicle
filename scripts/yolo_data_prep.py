# §6  Main Entry Point
# ═══════════════════════════════════════════════════════════════════════════════

def main() -> None:
    """Parse sub-commands and dispatch to the appropriate handler."""
    parser = argparse.ArgumentParser(
        prog="yolo_data_prep",
        description="Dataset preparation tools for the YOLO face-detection pipeline.",
    )
    parser.add_argument("--verbose", action="store_true", help="Enable debug logging.")
    parser.add_argument(
        "--data-dir", type=Path, default=None,
        help="Dataset root directory. Defaults to <project_root>/datasets/faces.",
    )

    subparsers = parser.add_subparsers(dest="command", help="Sub-command to execute.")

    # ── scaffold ─────────────────────────────────────────────────────────
    subparsers.add_parser("scaffold", help="Create the dataset directory structure.")

    # ── split ────────────────────────────────────────────────────────────
    split_parser = subparsers.add_parser("split", help="Split images into train/val sets.")
    split_parser.add_argument("--source", type=str, required=True, help="Source directory of images + labels.")
    split_parser.add_argument("--ratio", type=float, default=0.2, help="Validation set ratio (default: 0.2).")
    split_parser.add_argument("--seed", type=int, default=42, help="Random seed (default: 42).")

    # ── convert ──────────────────────────────────────────────────────────
    conv_parser = subparsers.add_parser("convert", help="Convert COCO/VOC annotations to YOLO format.")
    conv_parser.add_argument("--format", type=str, required=True, choices=["coco", "voc"], help="Source format.")
    conv_parser.add_argument("--input", type=str, required=True, help="Input file (COCO JSON) or directory (VOC XML).")
    conv_parser.add_argument("--output-dir", type=str, default=None, help="Output directory for YOLO labels.")
    conv_parser.add_argument("--class-names", type=str, default="face", help="Comma-separated class names.")

    # ── augment ──────────────────────────────────────────────────────────
    aug_parser = subparsers.add_parser("augment", help="Generate augmented training samples.")
    aug_parser.add_argument("--multiplier", type=int, default=3, help="Augmented copies per image (default: 3).")
    aug_parser.add_argument("--image-size", type=int, default=640, help="Target resolution (default: 640).")

    # ── stats ────────────────────────────────────────────────────────────
    subparsers.add_parser("stats", help="Print dataset statistics.")

    args = parser.parse_args()

    if not args.command:
        parser.print_help()
        sys.exit(0)

    cfg = YoloConfig(data_dir=args.data_dir, verbose=args.verbose)
    logger = setup_logger(verbose=cfg.verbose)

    # Dispatch to the correct sub-command handler.
    dispatch = {
        "scaffold": cmd_scaffold,
        "split": cmd_split,
        "convert": cmd_convert,
        "augment": cmd_augment,
        "stats": cmd_stats,
    }
    dispatch[args.command](args, cfg, logger)


if __name__ == "__main__":
    main()
