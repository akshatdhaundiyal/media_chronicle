# §5  Sub-command: stats
# ═══════════════════════════════════════════════════════════════════════════════

def cmd_stats(args: argparse.Namespace, cfg: YoloConfig, logger) -> None:
    """Print a statistical summary of the dataset.

    Reports:
        • Total image count per split (train / val).
        • Total annotation count per split.
        • Class distribution (how many bboxes per class).
        • Bounding box size statistics (mean, min, max width/height).
    """
    data_dir = cfg.data_dir

    for split in ("train", "val"):
        img_dir = data_dir / split / "images"
        lbl_dir = data_dir / split / "labels"

        if not img_dir.exists():
            logger.warning("Missing split: %s", img_dir)
            continue

        images = [f for f in img_dir.iterdir() if f.suffix.lower() in SUPPORTED_EXTENSIONS]
        labels = list(lbl_dir.glob("*.txt")) if lbl_dir.exists() else []

        # Parse all label files to collect class and size statistics.
        class_counts: dict[int, int] = {}
        widths: list[float] = []
        heights: list[float] = []
        total_bboxes = 0

        for lbl_file in labels:
            for line in lbl_file.read_text(encoding="utf-8").strip().splitlines():
                parts = line.strip().split()
                if len(parts) < 5:
                    continue
                cls_id = int(parts[0])
                w, h = float(parts[3]), float(parts[4])

                class_counts[cls_id] = class_counts.get(cls_id, 0) + 1
                widths.append(w)
                heights.append(h)
                total_bboxes += 1

        # ── Print summary ────────────────────────────────────────────────
        logger.info("─── %s split ───", split.upper())
        logger.info("  Images:      %d", len(images))
        logger.info("  Label files: %d", len(labels))
        logger.info("  Bounding boxes: %d", total_bboxes)

        if class_counts:
            logger.info("  Class distribution:")
            for cls_id in sorted(class_counts):
                logger.info("    class %d: %d boxes", cls_id, class_counts[cls_id])

        if widths:
            logger.info("  BBox widths  — mean: %.4f, min: %.4f, max: %.4f", 
                        sum(widths)/len(widths), min(widths), max(widths))
            logger.info("  BBox heights — mean: %.4f, min: %.4f, max: %.4f",
                        sum(heights)/len(heights), min(heights), max(heights))


