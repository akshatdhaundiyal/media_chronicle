SUPPORTED_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


def cmd_split(args: argparse.Namespace, cfg: YoloConfig, logger) -> None:
    """Split a flat image+label directory into train and validation sets.

    Expects the source directory to contain image files alongside
    identically-named ``.txt`` label files. Files are shuffled with
    a fixed seed and split according to ``--ratio`` (fraction for val).

    Args (from CLI):
        --source:  Path to the flat directory of images + labels.
        --ratio:   Fraction of files to allocate to the validation set
                   (default: 0.2 = 20%).
        --seed:    Random seed for reproducible shuffling.
    """
    source = Path(args.source)
    if not source.is_dir():
        logger.error("Source directory not found: %s", source)
        sys.exit(1)

    # ── Collect image files ──────────────────────────────────────────────
    image_files = sorted(
        f for f in source.iterdir()
        if f.suffix.lower() in SUPPORTED_EXTENSIONS
    )

    if not image_files:
        logger.error("No images found in %s", source)
        sys.exit(1)

    # ── Shuffle and split ────────────────────────────────────────────────
    random.seed(args.seed)
    random.shuffle(image_files)

    val_count = max(1, int(len(image_files) * args.ratio))
    val_files = image_files[:val_count]
    train_files = image_files[val_count:]

    logger.info("Splitting %d images → %d train + %d val", len(image_files), len(train_files), len(val_files))

    # ── Copy files into the scaffolded structure ─────────────────────────
    data_dir = cfg.data_dir

    for split_name, file_list in [("train", train_files), ("val", val_files)]:
        img_dir = data_dir / split_name / "images"
        lbl_dir = data_dir / split_name / "labels"
        img_dir.mkdir(parents=True, exist_ok=True)
        lbl_dir.mkdir(parents=True, exist_ok=True)

        for img_path in file_list:
            # Copy image.
            shutil.copy2(img_path, img_dir / img_path.name)

            # Copy matching label file if it exists.
            label_path = img_path.with_suffix(".txt")
            if label_path.exists():
                shutil.copy2(label_path, lbl_dir / label_path.name)
            else:
                # Create an empty label (negative sample — no detections).
                (lbl_dir / label_path.name).write_text("", encoding="utf-8")
                logger.debug("No label for %s — created empty negative sample.", img_path.name)

    logger.info("Split complete. Files copied to: %s", data_dir)


