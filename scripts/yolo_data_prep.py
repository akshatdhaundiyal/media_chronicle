def cmd_scaffold(args: argparse.Namespace, cfg: YoloConfig, logger) -> None:
    """Create the canonical YOLO dataset directory structure.

    This generates the following tree under ``cfg.data_dir``::

        datasets/faces/
        ├── data.yaml
        ├── train/
        │   ├── images/
        │   └── labels/
        └── val/
            ├── images/
            └── labels/

    The ``data.yaml`` file is auto-populated with a single ``face`` class
    and relative paths. Edit it manually if your dataset has multiple classes.
    """
    data_dir = cfg.data_dir
    logger.info("Scaffolding dataset directory: %s", data_dir)

    # Create directory tree.
    for split in ("train", "val"):
        for subdir in ("images", "labels"):
            (data_dir / split / subdir).mkdir(parents=True, exist_ok=True)

    # Generate a starter data.yaml if it doesn't exist.
    data_yaml = data_dir / "data.yaml"
    if not data_yaml.exists():
        yaml_content = (
            f"# YOLOv8 Dataset Configuration\n"
            f"# Auto-generated by yolo_data_prep.py scaffold\n"
            f"path: {data_dir.resolve()}\n"
            f"train: train/images\n"
            f"val: val/images\n"
            f"\n"
            f"# Class definitions (edit as needed)\n"
            f"names:\n"
            f"  0: face\n"
        )
        data_yaml.write_text(yaml_content, encoding="utf-8")
        logger.info("Created data.yaml with 1 class: face")
    else:
        logger.info("data.yaml already exists — skipping creation.")

    logger.info("Dataset scaffold complete. Place your images and labels into:")
    logger.info("  train: %s", data_dir / "train")
    logger.info("  val:   %s", data_dir / "val")


