# §4  Sub-command: augment
# ═══════════════════════════════════════════════════════════════════════════════

def cmd_augment(args: argparse.Namespace, cfg: YoloConfig, logger) -> None:
    """Generate augmented copies of training images with consistent bbox transforms.

    For each training image, this creates ``--multiplier`` augmented variants
    (default: 3). Both the augmented image and its transformed labels are
    saved alongside the originals.

    This is an offline augmentation step. Ultralytics also supports online
    augmentation during training (the default), so this command is optional
    and useful for small datasets where you want to inflate the training set.

    Args (from CLI):
        --multiplier:  Number of augmented copies per original image (default: 3).
        --image-size:  Target resolution for the augmentation pipeline.
    """
    import cv2
    import numpy as np
    from utils.data_transforms import build_augmentation_pipeline

    train_img_dir = cfg.data_dir / "train" / "images"
    train_lbl_dir = cfg.data_dir / "train" / "labels"

    if not train_img_dir.exists():
        logger.error("Training images directory not found: %s", train_img_dir)
        sys.exit(1)

    # ── Build augmentation pipeline ──────────────────────────────────────
    pipeline = build_augmentation_pipeline(image_size=args.image_size)

    # ── Collect original images ──────────────────────────────────────────
    image_files = sorted(
        f for f in train_img_dir.iterdir()
        if f.suffix.lower() in SUPPORTED_EXTENSIONS and "_aug" not in f.stem
    )

    logger.info(
        "Augmenting %d images × %d = %d new samples",
        len(image_files), args.multiplier, len(image_files) * args.multiplier,
    )

    created = 0
    for img_path in image_files:
        # Read image.
        img = cv2.imread(str(img_path))
        if img is None:
            logger.warning("Could not read image: %s", img_path.name)
            continue
        img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

        # Read YOLO labels.
        label_path = train_lbl_dir / (img_path.stem + ".txt")
        bboxes: list[list[float]] = []
        class_labels: list[int] = []

        if label_path.exists():
            for line in label_path.read_text(encoding="utf-8").strip().splitlines():
                parts = line.strip().split()
                if len(parts) >= 5:
                    class_labels.append(int(parts[0]))
                    bboxes.append([float(v) for v in parts[1:5]])

        # Generate augmented copies.
        for aug_idx in range(args.multiplier):
            try:
                augmented = pipeline(
                    image=img_rgb,
                    bboxes=bboxes,
                    class_labels=class_labels,
                )
            except Exception as exc:
                logger.debug("Augmentation failed for %s (variant %d): %s", img_path.name, aug_idx, exc)
                continue

            aug_img = augmented["image"]
            aug_bboxes = augmented["bboxes"]
            aug_labels = augmented["class_labels"]

            # Save augmented image.
            aug_stem = f"{img_path.stem}_aug{aug_idx}"
            aug_img_path = train_img_dir / f"{aug_stem}{img_path.suffix}"
            cv2.imwrite(str(aug_img_path), cv2.cvtColor(aug_img, cv2.COLOR_RGB2BGR))

            # Save augmented labels.
            aug_lbl_path = train_lbl_dir / f"{aug_stem}.txt"
            lines = []
            for cls, bbox in zip(aug_labels, aug_bboxes):
                lines.append(f"{cls} {bbox[0]:.6f} {bbox[1]:.6f} {bbox[2]:.6f} {bbox[3]:.6f}")
            aug_lbl_path.write_text("\n".join(lines), encoding="utf-8")

            created += 1

    logger.info("Augmentation complete: %d new samples created.", created)


