# yolo_data_prep.py — Dataset Preparation & Annotation Pipeline
# ──────────────────────────────────────────────────────────────────────────────
#
# Automates the creation, validation, and augmentation of a YOLO-format face
# detection dataset from raw image collections. Handles:
#
#   1. Directory scaffolding  — Creates the expected train/val/images/labels
#      folder structure.
#   2. Train/val splitting    — Splits a flat image+label directory into
#      stratified train and validation sets.
#   3. Annotation conversion  — Converts COCO JSON or Pascal VOC XML labels
#      to YOLO normalised `.txt` format.
#   4. Offline augmentation   — Generates augmented copies of training images
#      with geometrically consistent bbox transforms.
#   5. Dataset statistics     — Prints a summary of class distributions,
#      image counts, and bbox size histograms.
#
# Usage:
#     uv run scripts/yolo_data_prep.py scaffold
#     uv run scripts/yolo_data_prep.py split --source ./raw_faces/ --ratio 0.2
#     uv run scripts/yolo_data_prep.py convert --format coco --input annotations.json
#     uv run scripts/yolo_data_prep.py augment --multiplier 3
#     uv run scripts/yolo_data_prep.py stats
# ──────────────────────────────────────────────────────────────────────────────

