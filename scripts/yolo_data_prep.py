def cmd_convert(args: argparse.Namespace, cfg: YoloConfig, logger) -> None:
    """Convert annotations from COCO JSON or Pascal VOC XML to YOLO format.

    The converted ``.txt`` label files are written to the train/labels
    directory by default.

    Args (from CLI):
        --format:       Source annotation format ('coco' or 'voc').
        --input:        Path to the COCO JSON file or VOC XML directory.
        --output-dir:   Directory for the output YOLO labels.
        --class-names:  Comma-separated list of class names.
    """
    fmt = args.format.lower()
    input_path = Path(args.input)
    output_dir = Path(args.output_dir) if args.output_dir else (cfg.data_dir / "train" / "labels")
    class_names = [c.strip() for c in args.class_names.split(",")] if args.class_names else ["face"]

    logger.info("Converting %s → YOLO format", fmt.upper())
    logger.info("Classes: %s", class_names)

    if fmt == "coco":
        if not input_path.is_file():
            logger.error("COCO JSON file not found: %s", input_path)
            sys.exit(1)
        stats = coco_to_yolo(input_path, output_dir, class_names=class_names)
    elif fmt == "voc":
        if not input_path.is_dir():
            logger.error("VOC XML directory not found: %s", input_path)
            sys.exit(1)
        stats = voc_to_yolo(input_path, output_dir, class_names=class_names)
    else:
        logger.error("Unsupported format: '%s'. Use 'coco' or 'voc'.", fmt)
        sys.exit(1)

    logger.info("Conversion stats: %s", stats)


